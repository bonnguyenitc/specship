import re
import unicodedata


def slugify(text: str) -> str:
    # fold accents to ASCII first, before any filtering
    text = unicodedata.normalize("NFKD", text)
    text = "".join(c for c in text if not unicodedata.combining(c))
    text = text.lower()
    text = re.sub(r"[^a-z0-9]+", "-", text)
    return text.strip("-")
